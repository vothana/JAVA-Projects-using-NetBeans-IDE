public class Human {
    public int id;
    public String name;
    public String gender;
    public String dob;
    public String PhoneNumber;
    public String Email;
    public String Address;

    public Human(int id, String name, String gender, String dob, String PhoneNumber, String Email, String Address) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.dob = dob;
        this.PhoneNumber = PhoneNumber;
        this.Email = Email;
        this.Address = Address;
    }
}
